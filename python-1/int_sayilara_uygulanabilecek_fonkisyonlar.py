""" integer sayılar için kullanıla bilecek fonkisyonlar"""

# pow --> int sayıların üssünü almak için kullanılır
print(pow(2,4))

# sum --> içine yazdığımız sayıları toplar
print(sum((1,2,3,4,)))

# max & min --> içindeki en büyük ve en küçük sayıyı bulur.
print(max(123,141,331,465))
print(min(123,141,331,465))

# round --> ondalıklı bir sayıyı en yakın tam sayı değerinede yazer.
print(round(5.43))
print(round(7.6))

# abs --> mutlak değerini verir.
print(abs(-4))
